#include "drv_led.h" 


/**********************************************************************************************************
*函数原型: void LED_Init(void)
*函数功能: LED IO初始化
*输入参数: pvParameters
*返回数据: none
*修改日期: 2018-11-17
*备注信息：
**********************************************************************************************************/

void LED_Init(void)
{    	 
    RCC->AHB1ENR|=1<<1;	//使能PORTB时钟 
	RCC->AHB1ENR|=1<<2;	//使能PORTC时钟 
	GPIO_Set(GPIOB,PIN1,GPIO_MODE_OUT,GPIO_OTYPE_PP,GPIO_SPEED_100M,GPIO_PUPD_PU); //PB1设置
	GPIO_Set(GPIOC,PIN6|PIN7,GPIO_MODE_OUT,GPIO_OTYPE_PP,GPIO_SPEED_100M,GPIO_PUPD_PU); //PB1设置
	LED0(1);
	LED1(1);
	LED2(1);
}







